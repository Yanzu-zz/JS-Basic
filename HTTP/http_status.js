/**
 * HTTP 状态码
 *    1xx 服务器收到请求
 *    2xx 表示成功处理请求
 *    3xx 重定向，浏览器直接（临时or永久）跳转
 *    4xx 客户端请求错误
 *    5xx 服务端错误
 * 
 * 
 * 常见状态码
 *    状态码有很多个，我们记住常见的状态码即可
 *    200 成功
 *    301 永久重定向（配合 location，浏览器自动处理）
 *    302 临时重定向（配合 location，浏览器自动处理）
 *    304 资源未被修改
 *    403 没有权限
 *    404 资源未被找到
 *    500 服务器错误
 *    504 网关超时
 * 
 * 
 * 关于协议和规范
 *    就是一个约定，要求大家都跟着执行
 *    不要违反规范，例如 IE 浏览器
 */